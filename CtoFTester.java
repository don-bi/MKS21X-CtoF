public class CtoF{
  public static void main(String[] args){

  }

  public static double celsiusToFahrenheit(double celsius){
    fahrenheit = celsius * (9.0 / 5.0) + 32.0;
    return fahrenheit;
  }

  public static double fahrenheitToCelsius(){
    celsius = (fahrenheit - 32.0) * (5.0 / 9.0);
    return celsius;
  }
}
